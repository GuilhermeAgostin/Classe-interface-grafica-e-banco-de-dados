package Model;

public class Perfil 
{
    public int IdPerfil;
    public String Nome;

    public Perfil() {
		
		
	}

    
    public int getIdPerfil() {
        return IdPerfil;
    }
    public void setIdPerfil(int idPerfil) {
        IdPerfil = idPerfil;
    }
    public String getNome() {
        return Nome;
    }
    public void setNome(String nome) {
        Nome = nome;
    }
   
    
}
